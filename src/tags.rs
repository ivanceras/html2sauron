use lazy_static::lazy_static;

macro_rules! valid_tags {
    ( $(
         $(#[$attr:meta])*
         $name:ident;
       )*
     ) => {
        lazy_static!{
        static ref VALID_TAGS:Vec<&'static str> =
            vec![
                $(
                  stringify!($name)
                 ),*
            ];
        }
    }
}
